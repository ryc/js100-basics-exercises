// conditionals - ex6.js

let weather = 'snowy';

switch (weather) {
  case 'sunny':
    console.log("It's a beautiful day!");
    break;
  case 'rainy':
    console.log('Grab your umbrella.');
    break;
  case 'snowy':
    console.log('Time for a run!');
    break;
  default:
    console.log('Run anyway.');
}